int main() {
    int N = 8; // 入力は固定で8x8
    vector<string> A(N);
    
    // 入力を受け取る
    for (int i = 0; i < N; ++i) {
        cin >> A[i];
    }

    // 出力用の新しい配列Bを初期化
    vector<string> B(N, string(N, '.'));

    // 入力に基づいて新しいパターンを構築
    for (int i = 0; i < N; ++i) {
        for (int j = 0; j < N; ++j) {
            if (A[i][j] == '#') {
                // 対称的に#を配置する
                B[j][N - 1 - i] = '#'; // 90度時計回りに配置
            }
        }
    }

    // 変換された行列を出力
    for (int i = 0; i < N; ++i) {
        cout << B[i] << endl;
    }

    return 0;
}